GM_RSSP1_BOOL GM_RSSP1_CRC_Calculate_CRC16(const GM_RSSP1_UINT8 *pBuffer, GM_RSSP1_UINT32 bufferLen, GM_RSSP1_UINT16* pCRC, const GM_RSSP1_UINT16* pCRCTable)
{
	/** @brief 待处理 */
	GM_RSSP1_UINT32 i = 0U;
	GM_RSSP1_UINT16 x = 0U;

	if ((NULL == pBuffer) || (NULL == pCRCTable) || (NULL == pCRC))
	{
		return GM_RSSP1_FALSE;
	}

	for (i = (GM_RSSP1_UINT32)0u; i < bufferLen; ++i)
	{
		x = pCRCTable[((1U << 8U) - 1U) & (x ^ (GM_RSSP1_UINT16)(pBuffer[i]))] ^((x >> 8U) & 0xFFFFU);
	}

	*pCRC = (GM_RSSP1_UINT16)(0xFFFFU & x);

	return GM_RSSP1_TRUE;
}

GM_RSSP1_BOOL GM_RSSP1_CRC_Calculate_CRC32(const GM_RSSP1_UINT8* pBuffer, GM_RSSP1_UINT32 bufferLen, GM_RSSP1_UINT32* pCRC, const GM_RSSP1_UINT32* pCRCTable)
{
    GM_RSSP1_UINT32 i = 0U;
    GM_RSSP1_UINT32 x = 0U;
    GM_RSSP1_UINT8 y  = 0U;

    if ((NULL == pCRCTable) || (NULL == pCRC) || (NULL == pBuffer))
    {
        return GM_RSSP1_FALSE;
    }

    for (i = (GM_RSSP1_UINT32)0U; i < bufferLen; ++i)
    {
        y = GM_RSSP1_CRC_TabReflect[pBuffer[i]];
        x = (x >> 8U) ^ pCRCTable[(x ^ y) & 0xFFU];
    }

    *pCRC = x;

    return GM_RSSP1_TRUE;
}

