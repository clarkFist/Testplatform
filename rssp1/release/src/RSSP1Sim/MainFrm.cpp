LRESULT CMainFrame::OnPrompt(WPARAM wParam, LPARAM lParam)
{
	CString* pstr = (CString*)wParam;
	ASSERT(pstr);
	m_wndOutput.AddBuildWindow(*pstr);
	if (lParam)
		m_wndOutput.ShowPane(TRUE, FALSE, TRUE);
	delete pstr;
	return 0;
}

